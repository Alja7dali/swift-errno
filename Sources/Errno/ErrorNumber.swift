    case .level3Halted:                             return EL3HLT
    case .level3Reset:                              return EL3RST
    case .linkNumberOutOfRange:                     return ELNRNG
    case .protocolDriverNotAttached:                return EUNATCH
    case .noCSIStructureAvailable:                  return ENOCSI
    case .level2Halted:                             return EL2HLT
    case .invalidExchange:                          return EBADE
    case .invalidRequestDescriptor:                 return EBADR
    case .exchangeFull:                             return EXFULL
