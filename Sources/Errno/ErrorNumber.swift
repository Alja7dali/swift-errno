    case .tooManyLevelsOfSymbolicLinks:             return ELOOP
    case .noMessageOfDesiredType:                   return ENOMSG
    case .identifierRemoved:                        return EIDRM
    case .channelNumberOutOfRange:                  return ECHRNG
